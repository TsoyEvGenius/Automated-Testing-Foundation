/*---------------------------------------------------------------------------------------------  
 В терминале оплаты сохранено ваше имя, напишите функцию для определения 
 имени в терминале(если вы ввели ваше имя, то привет + имя, если нет, то нет такого имени)
-----------------------------------------------------------------------------------------------*/
const savedName = 'Jack'
function nameDefinition(enteredName) {
    if (enteredName === savedName) {
        return confirm(`Привет, ${enteredName}`)
    } else {
        return confirm('Нет такого имени')
    }
}
let enteredName = prompt('Введите имя')
nameDefinition(enteredName)



