/*--------------------------------------------------------------------
Напишите функцию, которая определяет является ли число простым или нет
-----------------------------------------------------------------------*/
function isPrime(num) {
    for (let i = 2; i <= Math.sqrt(num); i++) {
        if ( num % i === 0 ) {
            return false;
        }
    }
    return num > 1
}
console.log('4', isPrime(4));
console.log('5', isPrime(5));
