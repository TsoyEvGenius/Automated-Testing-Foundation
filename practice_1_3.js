let str = "Hello";
let num = 5;
let bool = true;

//Сложение 
console.log(str + bool);
console.log(str + num);
console.log(num + bool);

//Умножение
console.log(str * bool);
console.log(str * num);
console.log(num * bool);

//Деление
console.log(str / bool);
console.log(str / num);
console.log(num / bool);

//Преобразование
console.log(Number (str));
console.log(Boolean (str));
console.log(String (str));
